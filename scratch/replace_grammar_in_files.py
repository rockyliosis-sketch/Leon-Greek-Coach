def update_file(filepath):
    print(f"Updating {os.path.basename(filepath)}...")
    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()
        
    # Find getUnitGrammarPoints start
    func_start = content.find("const getUnitGrammarPoints")
    if func_start == -1:
        print("Could not find getUnitGrammarPoints function!")
        return False
        
    # Find grammarData start
    grammar_start = content.find("const grammarData: Record<string, Record<number, string>> = {", func_start)
    if grammar_start == -1:
        print("Could not find grammarData block!")
        return False
        
    # Find grammarData end by matching opening braces
    brace_count = 0
    i = grammar_start + len("const grammarData: Record<string, Record<number, string>> = {") - 1
    grammar_end = -1
    
    while i < len(content):
        char = content[i]
        if char == '{':
            brace_count += 1
        elif char == '}':
            brace_count -= 1
            if brace_count == 0:
                grammar_end = i + 1
                break
        i += 1
        
    if grammar_end == -1:
        print("Could not find closing brace of grammarData!")
        return False
        
    # Surgical replacement
    new_content = content[:grammar_start] + NEW_GRAMMAR_DATA_BLOCK + content[grammar_end:]
    
    with open(filepath, 'w', encoding='utf-8') as f:
        f.write(new_content)
        
    print(f"Successfully updated {os.path.basename(filepath)}")
    return True

