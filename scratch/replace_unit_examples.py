cleared_count = 0
updated_count = 0

for word in data["textbook_vocabulary"]:
    unit = word["unit"]
    if unit in target_units:
        greek_word = word["word_greek"]
        if unit in custom_sentences and greek_word in custom_sentences[unit]:
            gr_ex, zh_ex = custom_sentences[unit][greek_word]
            word["example_greek"] = gr_ex
            word["example_chinese"] = zh_ex
            updated_count += 1
        else:
            word["example_greek"] = ""
            word["example_chinese"] = ""
            cleared_count += 1

